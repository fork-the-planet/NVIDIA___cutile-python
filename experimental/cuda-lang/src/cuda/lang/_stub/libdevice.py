__doc__ = """
The libdevice library is a collection of NVVM bitcode functions that implement
common functions for NVIDIA GPU devices, including math primitives and
bit-manipulation functions. These functions are optimized for particular GPU
architectures, and are intended to be linked with an NVVM IR module during
compilation to PTX.

See `the official libdevice documentation here <LIBDEVICE_>`_.

.. note::

    The raw entrypoints have the same names as the NVVM bitcode entrypoints
    (with the ``__nv_`` prefix).
    Functions without the ``__nv_`` prefix provide more Pythonic interfaces
    if appropriate, otherwise they are aliases for the raw entrypoints.

    Examples:

    .. testcode::
        :template: kernel_wrapper.py

        # __nv_sincosf takes pointers but sincosf returns a tuple:
        sin, cos = cl.libdevice.sincosf(5.0)

        # __nv_abs and abs are aliases:
        abs_x = cl.libdevice.abs(-5)
        abs_x = cl.libdevice.__nv_abs(-5)

.. _LIBDEVICE: https://docs.nvidia.com/cuda/libdevice-users-guide/

"""

