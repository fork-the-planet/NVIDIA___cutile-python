@function
def frexpf(x: float32) -> tuple[float32, int32]:
    """Decompose ``x`` into a normalized fraction and an integral exponent."""
    with local_array(shape=1, dtype=int32) as exp:
        frac = __nv_frexpf(x, exp.get_element_pointer(0))
    return frac, exp[0]


